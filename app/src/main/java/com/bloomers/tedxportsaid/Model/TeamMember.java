package com.bloomers.tedxportsaid.Model;

public class TeamMember {

    private String desc;
    private String name;
    private String profile_url;
    private String team;

    public String getDesc() {
        return desc;
    }

    public String getName() {
        return name;
    }

    public String getProfile_url() {
        return profile_url;
    }

    public String getTeam() {
        return team;
    }
}
